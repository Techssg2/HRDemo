        public static byte[] RandomSecretKey()
        {
            var rng = new RNGCryptoServiceProvider();
            var key = new byte[24];
            rng.GetBytes(key);
            for (var i = 0; i < key.Length; ++i)
            {
                int keyByte = key[i] & 0xFE;
                var parity = 0;
                for (var b = keyByte; b != 0; b >>= 1) parity ^= b & 1;
                key[i] = (byte)(keyByte | (parity == 0 ? 1 : 0));
            }
            return key;
        }
